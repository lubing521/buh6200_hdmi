hi_s32 pq_mng_save_hdr_cfg(hi_void)
{
    hi_s32 write_len = 0;
#ifdef PQ_HDR_CFG_DBG
    hi_u32 i = 0;
    hi_s16 date;
    hi_char str[32]; /* 32 :a number */

    pf_hdrcfg = filp_open("/mnt/scramble_hdr_cfg.cfg", O_RDWR | O_CREAT | O_APPEND, 0);
    if (!pf_hdrcfg) {
        return HI_FAILURE;
    }

    for (i = 0; i < 16 * PQ_HDR_ALG_TM_LUT_SIZE; i++) { /* 16 :a number */
        date = (i % 256) + g_as16_tm_work_lut[i]; /* 256 :a number */
        if (i % 32 != 31) { /* 32 :a number 31 :a number */
            snprintf(str, sizeof(str), "%d, ", date);
        } else {
            snprintf(str, sizeof(str), "%d,\r\n", date);
        }

        write_len = pf_hdrcfg->f_op->write(pf_hdrcfg, str, strlen(str), &pf_hdrcfg->f_pos);
    }

    if (pf_hdrcfg) {
        filp_close(pf_hdrcfg, NULL);
    }
#endif
    return write_len;
}

hi_s32 pq_mng_descramble_hdr_cfg(pq_hdr_alg_cfg_type type, hi_void* date)
{
    hi_u32 i, j;
    hi_s16* ps16_date = HI_NULL;

    if (date == HI_NULL) {
        return HI_FAILURE;
    }

    ps16_date = (hi_s16*)date;

    if (type == PQ_HDR_ALG_CFG_TYPE_TM) {
        for (i = 0; i < 16 * PQ_HDR_ALG_TM_LUT_SIZE; i++) { /* 16 :a number */
            g_as16_tm_default_lut[i] = g_as16_tm_default_lut[i] - (i % 256); /* 256 :a number */
        }

        for (i = 0; i < 16; i++) { /* 16 :a number */
            for (j = 0; j < PQ_HDR_ALG_TM_LUT_SIZE; j++) {
                *(ps16_date + i * PQ_HDR_ALG_TM_LUT_SIZE + j) = g_as16_tm_default_lut[16 * j + i]; /* 16 :a number */
            }
        }

        return (PQ_HDR_ALG_TM_LUT_SIZE * 16); /* 16 :a number */
    }

    return HI_SUCCESS;
}

hi_s32 pq_mng_scramble_hdr_cfg(pq_hdr_alg_cfg_type type)
{
#ifdef PQ_HDR_CFG_DBG
    hi_u32 i = 0;

    if (type == PQ_HDR_ALG_CFG_TYPE_TM) {
        for (i = 0; i < PQ_HDR_ALG_TM_LUT_SIZE; i++) {
            g_as16_tm_work_lut[16 * i + 0] = g_tm_lut_i[i]; /* 16 :a number */
            g_as16_tm_work_lut[16 * i + 1] = g_tm_lut_[i]; /* 16 :a number */
            g_as16_tm_work_lut[16 * i + 2] = g_sm_lut_i[i]; /* 16 :a number 2 :a number */
            g_as16_tm_work_lut[16 * i + 3] = g_sm_lut_[i]; /* 16 :a number 3 :a number */
        }

        pq_mng_save_hdr_cfg();
    }
#endif
    return HI_SUCCESS;
}





