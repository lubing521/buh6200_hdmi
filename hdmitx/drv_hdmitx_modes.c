hi_u32 vic2table_index(hi_u32 vic)
{
    if (vic > 0 && vic <= VIC_5120X2160P100_64_27) {
        return vic;
    } else if (vic >= VIC_5120X2160P120_64_27 && vic <= VIC_4096X2160P120_256_135) {
        return vic - 65; /* 65 = (192-128+1) */
    } else if (vic >= VIC_VESA_800X600P60 && vic <= VIC_VESA_1680X1050P60RB) {
        return vic - 102; /* 102 = (256-220+1)-(192-128 + 1) */
    } else {
        return 0;
    }
}

struct hdmi_timing_mode *vic2timing_mode(hi_u32 vic)
{
    hi_u32 idx;

    idx = vic2table_index(vic);
    if (idx > 0 && (idx < ARRAY_SIZE(g_timing_mode_table))) {
        return &g_timing_mode_table[idx];
    } else {
        return NULL;
    }
}

struct frl_requirements *frl_req_search_by_band(struct band_mode *in)
{
    hi_u32 i;
    struct band_mode band;

    if (!in) {
        return NULL;
    }

    band.vic = in->vic;
    band.color_depth = in->color_depth;
    band.color_format = (in->color_format == RGB444) ? YCBCR444 : in->color_format;

    if (vic2table_index(band.vic)) {
        for (i = 0; i < ARRAY_SIZE(g_frl_req_table); i++) {
            if (band.vic == g_frl_req_table[i].band_mode.vic &&
                band.color_format == g_frl_req_table[i].band_mode.color_format &&
                band.color_depth == g_frl_req_table[i].band_mode.color_depth) {
                return &g_frl_req_table[i];
            }
        }
    }

    return NULL;
}

/*
 * hdmi_mode_create - create a new display mode
 * @hdmi_hdmi_hdmi_dev: DRM hdmi_hdmi_hdmi_device
 *
 * Create a new, cleared hdmi_display_mode with kzalloc, allocate an ID for it
 * and return it.
 *
 * Returns:
 * Pointer to new mode on success, NULL on error.
 */
struct hdmi_display_mode *hdmi_mode_create(struct hisilicon_hdmi *hdmi_dev)
{
    struct hdmi_display_mode *nmode = HI_NULL;

    nmode = osal_kmalloc(HI_ID_HDMITX, sizeof(struct hdmi_display_mode), OSAL_GFP_KERNEL);
    if (!nmode) {
        HDMI_ERR("osal_kmalloc fail.\n");
        return HI_NULL;
    }

    if (memset_s(nmode, sizeof(struct hdmi_display_mode), 0, sizeof(struct hdmi_display_mode))) {
        HDMI_ERR("memset_s fail.\n");
        osal_kfree(HI_ID_HDMITX, nmode);
        return HI_NULL;
    }

    return nmode;
}

void timing_mode2display_mode(struct hdmi_timing_mode *timing_mode,
                              struct hdmi_display_mode *d_mode)
{
    struct hdmi_detail *detail = HI_NULL;

    if (timing_mode == HI_NULL || d_mode == HI_NULL) {
        HDMI_ERR("ptr is null.");
        return;
    }

    detail = &d_mode->detail;

    d_mode->vic = timing_mode->vic;
    d_mode->detail.pixel_clock = timing_mode->pixel_clock;
    d_mode->detail.progressive = timing_mode->progressive;

    d_mode->detail.h_active = timing_mode->h_active;
    d_mode->detail.h_back = timing_mode->h_back;
    d_mode->detail.h_blank = timing_mode->h_blank;
    d_mode->detail.h_front = timing_mode->h_front;
    d_mode->detail.h_pol = timing_mode->h_pol;
    d_mode->detail.h_sync = timing_mode->h_sync;
    d_mode->detail.h_total = timing_mode->h_total;

    d_mode->detail.v_active = timing_mode->v_active;
    d_mode->detail.v_back = timing_mode->v_back;
    d_mode->detail.v_blank = timing_mode->v_blank;
    d_mode->detail.v_front = timing_mode->v_front;
    d_mode->detail.v_pol = timing_mode->v_pol;
    d_mode->detail.v_sync = timing_mode->v_sync;
    d_mode->detail.v_total = timing_mode->v_total;

    d_mode->detail.pic_asp_ratio = timing_mode->pic_aspect;

    d_mode->detail.field_rate = timing_mode->field_rate;

    if (strncpy_s(d_mode->name, sizeof(d_mode->name), timing_mode->name, sizeof(d_mode->name) - 1)) {
        HDMI_ERR("strncpy_s failed!\n");
    }
}

/*
 * hdmi_mode_create_from_vic - create a new display mode from vic
 * @hdmi_hdmi_hdmi_dev: DRM hdmi_hdmi_hdmi_device
 * @vic: vic to create the hdmi_display_mode
 *
 * Create a new, set hdmi_display_mode from g_timing_mode_table, allocate an ID for it
 * and return it.
 *
 * Returns:
 * Pointer to new mode on success, NULL on error.
 */
struct hdmi_display_mode *hdmi_mode_create_from_vic(struct hisilicon_hdmi *hdmi_dev, hi_u32 vic)
{
    struct hdmi_display_mode *nmode = HI_NULL;
    struct hdmi_timing_mode *timing_mode = HI_NULL;

    timing_mode = vic2timing_mode(vic);
    if (timing_mode == HI_NULL) {
        return HI_NULL;
    }

    nmode = osal_kmalloc(HI_ID_HDMITX, sizeof(struct hdmi_display_mode), OSAL_GFP_KERNEL);
    if (!nmode) {
        HDMI_ERR("osal_kmalloc fail.\n");
        return HI_NULL;
    }

    if (memset_s(nmode, sizeof(struct hdmi_display_mode), 0, sizeof(struct hdmi_display_mode))) {
        HDMI_ERR("memset_s fail.\n");
        osal_kfree(HI_ID_HDMITX, nmode);
        return HI_NULL;
    }

    timing_mode2display_mode(timing_mode, nmode);

    return nmode;
}

hi_bool hdmi_valid_mode_vic(hi_u32 vic)
{
    return (vic > 0 && vic <= VIC_5120X2160P100_64_27) ||
        (vic >= VIC_5120X2160P120_64_27 && vic <= VIC_4096X2160P120_256_135) ||
        (vic >= VIC_VESA_800X600P60 && vic < VIC_VESA_1680X1050P60RB);
}

